package com.company;

public class Ceiling {

    private int length;
    private int width;
    private String color;

    public Ceiling(int length, int width, String color) {
        this.length = length;
        this.width = width;
        this.color = color;
    }

    public int getLength() {
        return length;
    }

    public int getWidth() {
        return width;
    }

    public String getColor() {
        return color;
    }
}
